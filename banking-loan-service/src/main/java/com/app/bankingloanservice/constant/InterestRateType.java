package com.app.bankingloanservice.constant;

/**
 * Enumeration representing the types of interest rates for loans.
 */
public enum InterestRateType {
    /**
     * Fixed interest rate: The interest rate remains constant throughout the loan term.
     */
    FIXED,

    /**
     * Floating interest rate: The interest rate may vary over the loan term based on market conditions.
     */
    FLOATING
}
